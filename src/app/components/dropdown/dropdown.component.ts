import { Component, EventEmitter, Input, Output } from '@angular/core';
import { CommonModule } from '@angular/common'; // ðŸ‘ˆ Importa CommonModule

@Component({
  selector: 'app-dropdown',
  standalone: true,
  imports: [CommonModule],
  templateUrl: './dropdown.component.html',
  styleUrls: ['./dropdown.component.scss']
})
export class DropdownComponent {
  @Input() options: { label: string; image: string }[] = [];
  @Input() placeholder: string = 'Selecciona una opciÃ³n';
  @Output() selected = new EventEmitter<{ label: string; image: string }>();

  isOpen = false;
  selectedOption: { label: string; image: string } | null = null;

  // Alternar apertura/cierre del dropdown
  toggleDropdown() {
    this.isOpen = !this.isOpen;
  }

  // MÃ©todo para abrir el dropdown manualmente
  openDropdown() {
    this.isOpen = true;
  }

  // MÃ©todo para cerrar el dropdown manualmente
  closeDropdown() {
    this.isOpen = false;
  }

  // Seleccionar una opciÃ³n
  selectOption(option: { label: string; image: string }) {
    this.selectedOption = option;
    this.isOpen = false;
    this.selected.emit(option);
  }
}
